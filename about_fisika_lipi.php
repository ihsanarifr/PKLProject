									<p style="text-align: justify;">
										Pusat Penelitian Fisika - Lembaga Ilmu Pengetahuan Indonesia
										(P2FLIPI) pada awalnya bernama Lembaga Fisika Nasional (LFN)
										yang didirikan pada tahun 1967. Pada tahun 1986 dilakukan
										reorganisasi di lingkungan Lembaga Ilmu Pengetahuan Indonesia
										(LIPI) dimana sesuai dengan tugas dan fungsi barunya LFN
										berganti nama menjadi Pusat Penelitian dan Pengembangan Fisika
										Terapan (P3FT) hingga tahun 2001. Pada tahun 2001 kembali LIPI
										melakukan reorganisasi dimana P3FT menjadi Pusat Penelitian
										Fisika - Lembaga Ilmu Pengetahuan Indonesia (P2F-LIPI) hingga
										sekarang. Sebagai salah satu Lembaga Pemerintah
										Non-Kementerian (dahulu Lembaga Pemerintah Non Departemen
										(LPND)) eselon II maka perlu mempunyai dokumen Rencana
										Strategis (Renstra) yang memuat VISI, MISI, lingkungan
										strategis, kebijakan, dan arahan program P2F-LIPI. Dokumen
										rencana strategis ini, kemudian disebut Renstra Implementatif
										P2F-LIPI 2010-2014 merupakan panduan dan pijakan lembaga dan
										menjadi acuan bagi seluruh staf peneliti dan staf pendukungnya
										dalam melakukan kegiatan-kegiatannya lima tahun ke depan.
										Disamping itu dokumen ini juga merupakan acuan bagi
										pertanggungjawaban mengenai akuntabilitas kinerja instansi
										pemerintah dalam menjalankan tugas pokok dan fungsinya. Kepala
										Pusat Penelitian Fisika dari masa ke masa : <br>
									
									
