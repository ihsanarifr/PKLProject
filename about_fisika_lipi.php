						<br>Selamat datang di situs Pusat Penelitian Fisika ! Pusat
						Penelitian Fisika adalah nama baru dari Pusat Penelitian dan
						Pengembangan Fisika Terapan (P3FT) yang sebelumnya merupakan
						perubahan dari Lembaga Fisika Nasional (LFN) berdasarkan Keputusan
						Presiden RI No. 1 tanggal 13 Januari 1986. Lembaga ini merupakan
						salah satu Pusat Penelitian dibawah naungan Deputi Ilmu
						Pengetahuan dan Teknologi, Lembaga Ilmu Pengetahuan Indonesia
						(LIPI). Tujuan dan lingkup Puslit Fisika diarahkan pada usaha
						untuk mendukung pembangunan nasional melalui riset pengembangan
						sumber daya alam, terutama yang terkait dengan ilmu fisika, menuju
						Indonesia sebagai negara industri.
